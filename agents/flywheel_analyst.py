class FlywheelHealth(Enum):
    """Flywheel health status."""
    THRIVING = "thriving"
    HEALTHY = "healthy"
    WARNING = "warning"
    CRITICAL = "critical"
    STALLED = "stalled"


class PatternSeverity(Enum):
    """Failure pattern severity."""
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"


@dataclass
class FailurePattern:
    """Detected failure pattern."""
    error_code: str
    frequency: int
    first_seen: str
    last_seen: str
    affected_files: List[str]
    trend: str  # "increasing", "stable", "decreasing"
    severity: PatternSeverity
    recommendation: str
    
    def to_dict(self) -> Dict:
        return {
            **asdict(self),
            "severity": self.severity.value
        }


@dataclass
class FlywheelMetrics:
    """Metrics for a single flywheel."""
    name: str
    health: FlywheelHealth
    primary_metric: float
    secondary_metrics: Dict[str, float]
    trend: str  # "growing", "stable", "declining"
    last_updated: str
    insights: List[str]
    
    def to_dict(self) -> Dict:
        return {
            **asdict(self),
            "health": self.health.value
        }


@dataclass
class AmendmentProposal:
    """Constitutional amendment proposal."""
    proposal_id: str
    title: str
    problem_statement: str
    evidence: List[Dict]
    proposed_change: str
    affected_articles: List[str]
    impact_assessment: str
    rollout_steps: List[str]
    created: str
    status: str  # "draft", "pending_review", "approved", "rejected"
    
    def to_dict(self) -> Dict:
        return asdict(self)
    
    def to_markdown(self) -> str:
        """Generate markdown proposal document."""
        lines = [
            f"# Constitutional Amendment Proposal",
            f"",
            f"**Proposal ID**: {self.proposal_id}",
            f"**Status**: {self.status}",
            f"**Created**: {self.created}",
            f"",
            f"---",
            f"",
            f"## Problem Statement",
            f"",
            f"{self.problem_statement}",
            f"",
            f"---",
            f"",
            f"## Evidence",
            f""
        ]
        
        for i, ev in enumerate(self.evidence, 1):
            lines.append(f"### Evidence {i}")
            lines.append(f"- **Source**: {ev.get('source', 'N/A')}")
            lines.append(f"- **Date**: {ev.get('date', 'N/A')}")
            lines.append(f"- **Details**: {ev.get('details', 'N/A')}")
            lines.append("")
        
        lines.extend([
            f"---",
            f"",
            f"## Proposed Change",
            f"",
            f"{self.proposed_change}",
            f"",
            f"**Affected Articles**: {', '.join(self.affected_articles)}",
            f"",
            f"---",
            f"",
            f"## Impact Assessment",
            f"",
            f"{self.impact_assessment}",
            f"",
            f"---",
            f"",
            f"## Rollout Steps",
            f""
        ])
        
        for i, step in enumerate(self.rollout_steps, 1):
            lines.append(f"{i}. {step}")
        
        lines.extend([
            f"",
            f"---",
            f"",
            f"*Generated by Flywheel Analyst (Agent Omega)*",
            f"*Constitutional Authority: EPOS_CONSTITUTION_v3.1.md Article IX*"
        ])
        
        return "\n".join(lines)


