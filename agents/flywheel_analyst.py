    def _calculate_data_moat_flywheel(self) -> FlywheelMetrics:
        """Calculate Data Moat Flywheel metrics."""
        # Read vault registry
        vault_registry_path = self.epos_root / "context_vault" / "registry.json"
        
        vault_size_mb = 0.0
