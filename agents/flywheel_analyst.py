    # FAILURE PATTERN DETECTION
    # =========================================================================
    
    def detect_failure_patterns(self, days: int = 7) -> List[FailurePattern]:
        """
        Detect failure patterns from BI logs.
        
        Workflow: failure_pattern_detection
        """
        self._log(f"Analyzing failure patterns for last {days} days...")
        
        bi_data = self._get_bi_log()
        decisions = bi_data.get("decisions", [])
        
        # Filter to recent decisions
        cutoff = datetime.now() - timedelta(days=days)
        recent = []
        
        for d in decisions:
            try:
                ts = datetime.fromisoformat(d.get("timestamp", "2020-01-01"))
                if ts >= cutoff:
                    recent.append(d)
            except:
                continue
        
        # Aggregate error codes
        error_counts = Counter()
        error_files = {}
        error_timestamps = {}
        
        for d in recent:
            # From arbiter triage
            if d.get("agent") == "constitutional_arbiter":
                violations = d.get("violations_summary", {})
                for code, count in violations.items():
                    error_counts[code] += count
                    
                    if code not in error_files:
                        error_files[code] = set()
                    files = d.get("files", {}).get("rejected", [])
                    error_files[code].update(files)
                    
                    if code not in error_timestamps:
                        error_timestamps[code] = {"first": d["timestamp"], "last": d["timestamp"]}
                    else:
                        error_timestamps[code]["last"] = d["timestamp"]
        
        # Build patterns
        patterns = []
        
        for code, count in error_counts.most_common():
            # Determine severity
            if count >= self.PATTERN_THRESHOLDS["same_error_critical"]:
                severity = PatternSeverity.CRITICAL
            elif count >= self.PATTERN_THRESHOLDS["same_error_warning"]:
                severity = PatternSeverity.HIGH
            elif count >= 3:
                severity = PatternSeverity.MEDIUM
            else:
                severity = PatternSeverity.LOW
            
            # Generate recommendation
            recommendation = self._generate_recommendation(code, count)
            
            patterns.append(FailurePattern(
                error_code=code,
                frequency=count,
                first_seen=error_timestamps.get(code, {}).get("first", "unknown"),
                last_seen=error_timestamps.get(code, {}).get("last", "unknown"),
                affected_files=list(error_files.get(code, [])),
                trend="increasing" if count > 5 else "stable",
                severity=severity,
                recommendation=recommendation
            ))
        
        self._log(f"  Detected {len(patterns)} patterns")
        return patterns
    
    def _generate_recommendation(self, error_code: str, count: int) -> str:
        """Generate recommendation for an error pattern."""
        recommendations = {
            "ERR-PATH-001": "Add pre-commit hook to validate path formats before submission",
            "ERR-HEADER-001": "Add IDE snippet/template for constitutional file headers",
            "ERR-CONFIG-001": "Update project template to include load_dotenv() in all entrypoints",
            "ERR-CONTEXT-001": "Implement automatic vault ingestion for files >50KB",
            "ERR-SYNTAX-001": "Add syntax validation to CI pipeline before arbiter review",
            "ERR-TRUTH-001": "Refactor execution bridge to require proof objects in all responses"
        }
        
        base = recommendations.get(error_code, f"Review {error_code} in FAILURE_SCENARIOS.md")
        
        if count >= 10:
            base += ". CRITICAL: Consider constitutional amendment to address root cause."
        
        return base
    
